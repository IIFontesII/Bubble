function dots() {
   let right = document.getElementById('slide-cont').style.marginLeft = '0px';;

   if (right == '0px') {
       right = document.getElementById('slide-cont').style.marginLeft = '0px';
   } 
   
   document.getElementById('bg').style.backgroundColor = '#ccc';
   document.getElementById('bg1').style.backgroundColor = '#aaa';
   document.getElementById('bg2').style.backgroundColor = '#aaa';
   document.getElementById('bg3').style.backgroundColor = '#aaa';
}


